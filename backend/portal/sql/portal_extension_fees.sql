-- Academic Year
CREATE TABLE IF NOT EXISTS public.portal_academic_year (
  id        integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name      varchar(20) NOT NULL UNIQUE,          -- e.g. "2025-26"
  start_date date NOT NULL,
  end_date   date NOT NULL,
  is_active  boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- Fee Category master (configurable per school)
CREATE TABLE IF NOT EXISTS public.portal_fee_category (
  id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        varchar(100) NOT NULL UNIQUE,        -- Tuition, Admission, Transport …
  description text,
  is_active   boolean NOT NULL DEFAULT true,
  sort_order  integer NOT NULL DEFAULT 0
);

