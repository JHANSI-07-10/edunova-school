-- ---------------------------------------------------------------------------
-- 8. NEW TABLE: portal_exam_notification
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_exam_notification (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_schedule_id integer REFERENCES public.portal_exam_schedule(id) ON DELETE CASCADE,
  notification_type varchar(50) NOT NULL,
  title varchar(255) NOT NULL,
  message text NOT NULL,
  target_audience varchar(30) DEFAULT 'all',
  is_sent boolean DEFAULT false,
  sent_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- 9. NEW TABLE: portal_exam_audit
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_exam_audit (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_schedule_id integer REFERENCES public.portal_exam_schedule(id) ON DELETE SET NULL,
  action varchar(50) NOT NULL,
  action_by integer REFERENCES public.auth_user(id) ON DELETE SET NULL,
  details jsonb DEFAULT '{}',
  created_at timestamptz NOT NULL DEFAULT now()
);

