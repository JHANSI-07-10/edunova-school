-- Safe to re-run: every statement uses IF NOT EXISTS / IF EXISTS guards.
-- =============================================================================

-- ---------------------------------------------------------------------------
-- 1. NEW TABLE: portal_exam_type
-- ---------------------------------------------------------------------------
