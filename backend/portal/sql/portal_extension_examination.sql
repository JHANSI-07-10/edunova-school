-- ---------------------------------------------------------------------------
-- 2. NEW TABLE: portal_exam_subject
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_exam_subject (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_schedule_id integer NOT NULL REFERENCES public.portal_exam_schedule(id) ON DELETE CASCADE,
  subject_id integer NOT NULL REFERENCES public.portal_subject(id) ON DELETE CASCADE,
  exam_date date,
  start_time time,
  duration_minutes smallint DEFAULT 180,
  max_marks smallint DEFAULT 100,
  passing_marks smallint DEFAULT 35,
  UNIQUE (exam_schedule_id, subject_id)
);

-- ---------------------------------------------------------------------------
-- 3. NEW TABLE: portal_seating_arrangement
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_seating_arrangement (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_schedule_id integer NOT NULL REFERENCES public.portal_exam_schedule(id) ON DELETE CASCADE,
  student_id integer NOT NULL REFERENCES public.auth_user(id) ON DELETE CASCADE,
  room_name varchar(50) NOT NULL,
  seat_number varchar(20) NOT NULL,
  UNIQUE (exam_schedule_id, student_id)
);

-- ---------------------------------------------------------------------------
-- 4. NEW TABLE: portal_invigilator_allocation
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_invigilator_allocation (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_schedule_id integer NOT NULL REFERENCES public.portal_exam_schedule(id) ON DELETE CASCADE,
  teacher_id integer NOT NULL REFERENCES public.auth_user(id) ON DELETE CASCADE,
  room_name varchar(50) NOT NULL,
  exam_date date,
  start_time time,
  end_time time,
  UNIQUE (exam_schedule_id, teacher_id, room_name)
);

-- ---------------------------------------------------------------------------
-- 5. NEW TABLE: portal_marks_verification
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_marks_verification (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  result_id integer NOT NULL REFERENCES public.portal_result(id) ON DELETE CASCADE,
  verified_by integer REFERENCES public.auth_user(id) ON DELETE SET NULL,
  verified_at timestamptz NOT NULL DEFAULT now(),
  is_verified boolean DEFAULT true,
  remarks text DEFAULT ''
);

-- ---------------------------------------------------------------------------
-- 6. NEW TABLE: portal_grade_config
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.portal_grade_config (
