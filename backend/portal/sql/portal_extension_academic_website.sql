-- Run AFTER portal_extension_auth_user.sql
-- Safe to run multiple times: every statement is IF NOT EXISTS / ADD COLUMN IF NOT EXISTS.

-- Academic Levels
CREATE TABLE IF NOT EXISTS public.portal_academic_level (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(100) NOT NULL UNIQUE,
  description text DEFAULT '',
  icon_name varchar(50) DEFAULT '',
  sort_order integer DEFAULT 0,
  is_published boolean DEFAULT true
);

