-- =============================================================================
-- 9. LEARNING ANALYTICS SNAPSHOTS
-- =============================================================================
CREATE TABLE IF NOT EXISTS public.portal_learning_analytic (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id integer NOT NULL REFERENCES public.auth_user(id) ON DELETE CASCADE,
  course_id integer NOT NULL REFERENCES public.portal_course(id) ON DELETE CASCADE,
  total_lessons integer NOT NULL DEFAULT 0,
  completed_lessons integer NOT NULL DEFAULT 0,
  total_content_items integer NOT NULL DEFAULT 0,
  completed_content_items integer NOT NULL DEFAULT 0,
  total_live_classes integer NOT NULL DEFAULT 0,
  attended_live_classes integer NOT NULL DEFAULT 0,
  total_quizzes integer NOT NULL DEFAULT 0,
  quizzes_passed integer NOT NULL DEFAULT 0,
  average_quiz_score numeric(5,2) NOT NULL DEFAULT 0,
  total_assignments integer NOT NULL DEFAULT 0,
  assignments_submitted integer NOT NULL DEFAULT 0,
  total_homework integer NOT NULL DEFAULT 0,
  homework_submitted integer NOT NULL DEFAULT 0,
  total_learning_hours numeric(6,2) NOT NULL DEFAULT 0,
  overall_progress_percent numeric(5,2) NOT NULL DEFAULT 0,
  last_activity_at timestamptz,
  snapshot_date date NOT NULL DEFAULT CURRENT_DATE,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (student_id, course_id, snapshot_date)
);

