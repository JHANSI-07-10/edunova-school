# Ensure portal_assignment_rubric table exists (idempotent DDL)
# ---------------------------------------------------------------------------
def _ensure_rubric_table():
    if table_exists("portal_assignment_rubric"):
        return True
    try:
        with connection.cursor() as cursor:
            cursor.execute("""
                CREATE TABLE IF NOT EXISTS public.portal_assignment_rubric (
                    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    assignment_id integer NOT NULL REFERENCES public.portal_assignment(id) ON DELETE CASCADE,
                    criterion text NOT NULL,
                    max_marks smallint NOT NULL DEFAULT 10,
                    description text DEFAULT '',
                    sort_order smallint DEFAULT 0,
                    created_at timestamptz NOT NULL DEFAULT now()
                );
                CREATE INDEX IF NOT EXISTS idx_rubric_assignment ON public.portal_assignment_rubric(assignment_id);
            """)
        return True
    except Exception:
        return False


