# ---------------------------------------------------------------------------
class AssignmentSettingsView(AdminMixin, APIView):
    """GET  — global assignment settings
    POST — create or update settings"""

    SETTINGS_TABLE = "portal_assignment_settings"

    def _ensure_table(self):
        try:
            with connection.cursor() as cursor:
                cursor.execute("""
                    CREATE TABLE IF NOT EXISTS public.portal_assignment_settings (
                        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        setting_key varchar(100) NOT NULL UNIQUE,
                        setting_value text NOT NULL DEFAULT '',
                        description text DEFAULT '',
                        updated_at timestamptz NOT NULL DEFAULT now()
                    );
                """)
                # Seed defaults if empty
                cursor.execute("SELECT COUNT(*)::int FROM portal_assignment_settings")
                if cursor.fetchone()[0] == 0:
                    defaults = [
                        ("default_due_days", "7", "Default number of days until assignment is due"),
                        ("max_file_size_mb", "50", "Maximum file size in MB for submissions"),
                        ("allowed_file_types", "pdf,doc,docx,txt,jpg,png,zip", "Comma-separated allowed file types"),
                        ("late_policy", "penalize", "Late submission policy: allow/penalize/block"),
                        ("allow_resubmission", "true", "Whether resubmissions are allowed"),
                        ("max_resubmissions", "3", "Maximum number of resubmissions allowed"),
                        ("show_rubric_to_student", "true", "Whether rubric criteria are visible to students"),
                        ("auto_grade_quiz", "true", "Auto-grade quiz-type assignments"),
                    ]
                    for key, val, desc in defaults:
                        cursor.execute(
                            "INSERT INTO portal_assignment_settings (setting_key, setting_value, description) "
                            "VALUES (%s,%s,%s) ON CONFLICT DO NOTHING",
                            [key, val, desc],
                        )
        except Exception:
            pass

    def get(self, request):
        self._ensure_table()
        if not table_exists(self.SETTINGS_TABLE):
            return Response([])
        data = rows("SELECT * FROM portal_assignment_settings ORDER BY id")
        # Return as key-value dict for convenience
        settings = {d["setting_key"]: d["setting_value"] for d in data}
        return Response(serialise({"settings": settings, "raw": data}))

    def post(self, request):
        self._ensure_table()
        if not table_exists(self.SETTINGS_TABLE):
            return Response({"detail": "Settings table not found."}, status=400)
        d = request.data
        updates = d.get("settings") or d
        if not isinstance(updates, dict):
            return Response({"detail": "Provide settings as a key-value dict."}, status=400)
        updated_count = 0
        with connection.cursor() as cursor:
            for key, val in updates.items():
                if not key:
                    continue
                cursor.execute(
                    "INSERT INTO portal_assignment_settings (setting_key, setting_value, updated_at) "
                    "VALUES (%s,%s,now()) "
                    "ON CONFLICT (setting_key) DO UPDATE SET setting_value=EXCLUDED.setting_value, updated_at=now()",
                    [str(key), str(val)],
                )
                updated_count += 1
        log_action(request.user, "assignment_settings.update", self.SETTINGS_TABLE, 0,
                   {"updated_keys": updated_count})
        return Response({"detail": f"{updated_count} setting(s) saved."})


