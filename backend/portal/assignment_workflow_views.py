# 2. Assignment Scheduling (Admin + Teacher)
# ---------------------------------------------------------------------------
class AssignmentSchedulingView(APIView):
    """Full CRUD for assignments. Admin can manage all; Teacher can manage their own.
    GET    ?class_id=&subject_id=&teacher_id=&type= — list assignments
    POST   — create assignment
    PUT    — update assignment
    DELETE ?id= — delete assignment"""

    permission_classes = [IsAuthenticated]

    def _check_permission(self, request):
        role = get_role(request.user)
        if role not in ("Admin", "Teacher"):
            return False, role
        return True, role

    def get(self, request):
        if not table_exists("portal_assignment"):
            return Response([])
        d = request.query_params
        sql = """
            SELECT a.id, a.class_id, a.subject_id, a.teacher_id, a.title, a.description,
                   a.file_url, a.max_marks, a.due_date, a.assignment_type, a.quiz_questions,
                   s.name AS subject_name,
                   c.name || '-' || c.section AS class_name,
                   COALESCE(u.first_name || ' ' || u.last_name, u.username) AS teacher_name,
                   (SELECT COUNT(*)::int FROM portal_assignment_submission sub WHERE sub.assignment_id = a.id) AS submission_count
            FROM portal_assignment a
            LEFT JOIN portal_subject s ON s.id = a.subject_id
            LEFT JOIN portal_class c ON c.id = a.class_id
            LEFT JOIN auth_user u ON u.id = a.teacher_id
        """
        conditions = []
        params = []
        if d.get("class_id"):
            conditions.append("a.class_id = %s")
            params.append(d["class_id"])
        if d.get("subject_id"):
            conditions.append("a.subject_id = %s")
            params.append(d["subject_id"])
        if d.get("teacher_id"):
            conditions.append("a.teacher_id = %s")
            params.append(d["teacher_id"])
        if d.get("type"):
            conditions.append("a.assignment_type = %s")
            params.append(d["type"])
        if conditions:
            sql += " WHERE " + " AND ".join(conditions)
        sql += " ORDER BY a.due_date DESC NULLS LAST"
        return Response(serialise(rows(sql, params)))

    def post(self, request):
        ok, role = self._check_permission(request)
        if not ok:
            return Response({"detail": "Admin or Teacher role required."}, status=403)
        if not table_exists("portal_assignment"):
            return Response({"detail": "Assignment table not found."}, status=400)
        d = request.data
        title = (d.get("title") or "").strip()
        class_id = d.get("class_id")
        subject_id = d.get("subject_id")
        if not title or not class_id or not subject_id:
            return Response({"detail": "title, class_id, and subject_id are required."}, status=400)
        teacher_id = d.get("teacher_id") or request.user.id
        description = d.get("description", "")
        file_url = d.get("file_url", "")
        max_marks = d.get("max_marks", 100)
        due_date = d.get("due_date")
        assignment_type = d.get("assignment_type", "Assignment")
        quiz_questions = d.get("quiz_questions")
        if quiz_questions is not None and not isinstance(quiz_questions, str):
            quiz_questions = json.dumps(quiz_questions)

        with connection.cursor() as cursor:
            cursor.execute(
                "INSERT INTO portal_assignment "
                "(class_id, subject_id, teacher_id, title, description, file_url, max_marks, due_date, assignment_type, quiz_questions) "
                "VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s,%s) RETURNING id",
                [class_id, subject_id, teacher_id, title, description, file_url,
                 max_marks, due_date, assignment_type, quiz_questions],
            )
            new_id = cursor.fetchone()[0]
        log_action(request.user, "assignment.create", "portal_assignment", new_id,
                   {"title": title, "class_id": class_id, "subject_id": subject_id})
        return Response({"id": new_id, "detail": "Assignment created."}, status=201)

    def put(self, request):
        ok, role = self._check_permission(request)
        if not ok:
            return Response({"detail": "Admin or Teacher role required."}, status=403)
        if not table_exists("portal_assignment"):
            return Response({"detail": "Table not found."}, status=400)
        d = request.data
        rec_id = d.get("id")
        if not rec_id:
            return Response({"detail": "id is required."}, status=400)
        existing = row("SELECT id FROM portal_assignment WHERE id=%s", [rec_id])
        if not existing:
            return Response({"detail": "Assignment not found."}, status=404)
        # Teachers can only edit their own
        if role == "Teacher":
            own = row("SELECT teacher_id FROM portal_assignment WHERE id=%s", [rec_id])
            if own and own["teacher_id"] != request.user.id:
                return Response({"detail": "You can only edit your own assignments."}, status=403)

        fields = []
        vals = []
        for col in ("title", "description", "file_url", "max_marks", "due_date", "class_id",
                     "subject_id", "teacher_id", "assignment_type"):
            if col in d:
                fields.append(f"{col} = %s")
                vals.append(d[col])
        if "quiz_questions" in d:
            qq = d["quiz_questions"]
            if qq is not None and not isinstance(qq, str):
                qq = json.dumps(qq)
            fields.append("quiz_questions = %s")
            vals.append(qq)
        if not fields:
            return Response({"detail": "No fields to update."}, status=400)
        vals.append(rec_id)
        with connection.cursor() as cursor:
            cursor.execute(f"UPDATE portal_assignment SET {', '.join(fields)} WHERE id = %s", vals)
        log_action(request.user, "assignment.update", "portal_assignment", rec_id, d)
        return Response({"detail": "Updated."})

    def delete(self, request):
        ok, role = self._check_permission(request)
        if not ok:
            return Response({"detail": "Admin or Teacher role required."}, status=403)
        if not table_exists("portal_assignment"):
            return Response({"detail": "Table not found."}, status=400)
        rec_id = request.query_params.get("id")
        if not rec_id:
            return Response({"detail": "id is required."}, status=400)
        if role == "Teacher":
            own = row("SELECT teacher_id FROM portal_assignment WHERE id=%s", [rec_id])
            if own and own["teacher_id"] != request.user.id:
                return Response({"detail": "You can only delete your own assignments."}, status=403)
        with connection.cursor() as cursor:
            cursor.execute("DELETE FROM portal_assignment WHERE id=%s RETURNING id", [rec_id])
            deleted = cursor.fetchone()
        if not deleted:
            return Response({"detail": "Not found."}, status=404)
        log_action(request.user, "assignment.delete", "portal_assignment", rec_id, {})
        return Response({"detail": "Deleted."})


