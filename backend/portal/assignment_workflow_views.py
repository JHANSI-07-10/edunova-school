class LateSubmissionView(AdminMixin, APIView):
    """GET  — view late submission records and policy
    POST — configure late submission policy"""

    POLICY_TABLE = "portal_late_submission_policy"

    def _ensure_table(self):
        try:
            with connection.cursor() as cursor:
                cursor.execute("""
                    CREATE TABLE IF NOT EXISTS public.portal_late_submission_policy (
                        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        policy_name varchar(100) NOT NULL DEFAULT 'Default',
                        allow_late boolean DEFAULT true,
                        penalize_late boolean DEFAULT true,
                        penalty_percent numeric(5,2) DEFAULT 10.00,
                        max_late_days smallint DEFAULT 3,
                        block_after_max boolean DEFAULT false,
                        is_active boolean DEFAULT true,
                        updated_at timestamptz NOT NULL DEFAULT now(),
                        updated_by integer REFERENCES public.auth_user(id) ON DELETE SET NULL
                    );
                """)
                cursor.execute("SELECT COUNT(*)::int FROM portal_late_submission_policy")
                if cursor.fetchone()[0] == 0:
                    cursor.execute(
                        "INSERT INTO portal_late_submission_policy (policy_name, allow_late, penalize_late, penalty_percent, max_late_days) "
                        "VALUES ('Default', true, true, 10.00, 3)"
                    )
        except Exception:
            pass

    def get(self, request):
        self._ensure_table()
        if not table_exists(self.POLICY_TABLE):
            return Response([])
        # List late submissions with policy context
        late_submissions = []
        if table_exists("portal_assignment_submission") and table_exists("portal_assignment"):
            late_submissions = rows(
                """
                SELECT sub.id AS submission_id, sub.assignment_id, sub.student_id, sub.submitted_at,
                       a.due_date, a.title AS assignment_title,
                       COALESCE(u.first_name || ' ' || u.last_name, u.username) AS student_name,
                       CASE WHEN sub.submitted_at > a.due_date THEN
                           EXTRACT(DAY FROM sub.submitted_at - a.due_date)::int
                       ELSE 0 END AS days_late
                FROM portal_assignment_submission sub
                JOIN portal_assignment a ON a.id = sub.assignment_id
                JOIN auth_user u ON u.id = sub.student_id
                WHERE sub.submitted_at > a.due_date
                ORDER BY sub.submitted_at DESC
                LIMIT 100
                """
            )
        policy = rows("SELECT * FROM portal_late_submission_policy ORDER BY id") if table_exists(self.POLICY_TABLE) else []
        return Response(serialise({"policy": policy, "late_submissions": late_submissions}))

    def post(self, request):
        self._ensure_table()
        if not table_exists(self.POLICY_TABLE):
            return Response({"detail": "Policy table not found."}, status=400)
        d = request.data
        rec_id = d.get("id")
        with connection.cursor() as cursor:
            if rec_id:
                cursor.execute(
                    "UPDATE portal_late_submission_policy SET "
                    "policy_name=%s, allow_late=%s, penalize_late=%s, penalty_percent=%s, "
                    "max_late_days=%s, block_after_max=%s, is_active=%s, updated_at=now(), updated_by=%s WHERE id=%s",
                    [d.get("policy_name", "Default"), d.get("allow_late", True), d.get("penalize_late", True),
                     d.get("penalty_percent", 10), d.get("max_late_days", 3), d.get("block_after_max", False),
                     d.get("is_active", True), request.user.id, rec_id],
                )
                log_action(request.user, "late_policy.update", self.POLICY_TABLE, rec_id, d)
                return Response({"detail": "Policy updated."})
            else:
                cursor.execute(
                    "INSERT INTO portal_late_submission_policy "
                    "(policy_name, allow_late, penalize_late, penalty_percent, max_late_days, block_after_max, updated_by) "
                    "VALUES (%s,%s,%s,%s,%s,%s,%s) RETURNING id",
                    [d.get("policy_name", "Default"), d.get("allow_late", True), d.get("penalize_late", True),
                     d.get("penalty_percent", 10), d.get("max_late_days", 3), d.get("block_after_max", False),
                     request.user.id],
                )
                new_id = cursor.fetchone()[0]
                log_action(request.user, "late_policy.create", self.POLICY_TABLE, new_id, d)
                return Response({"id": new_id, "detail": "Policy created."}, status=201)


