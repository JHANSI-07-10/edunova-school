    def _completion_rate_report(self, class_id, subject_id):
        if not table_exists("portal_assignment"):
            return Response([])
        sql = """
            SELECT a.id, a.title, a.class_id, a.subject_id,
                   (SELECT COUNT(DISTINCT se.student_id) FROM portal_student_enrollment se WHERE se.class_id = a.class_id) AS enrolled,
                   (SELECT COUNT(*)::int FROM portal_assignment_submission sub WHERE sub.assignment_id = a.id) AS submitted
            FROM portal_assignment a WHERE 1=1
        """
        params = []
        if class_id:
            sql += " AND a.class_id = %s"
            params.append(class_id)
        if subject_id:
            sql += " AND a.subject_id = %s"
            params.append(subject_id)
