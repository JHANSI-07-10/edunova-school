class AssignmentNotificationView(AdminMixin, APIView):
    """GET  — list assignment notifications
    POST — create notification
    DELETE ?id= — delete notification"""

    def _ensure_table(self):
        try:
            with connection.cursor() as cursor:
                cursor.execute("""
                    CREATE TABLE IF NOT EXISTS public.portal_assignment_notification (
                        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        assignment_id integer REFERENCES public.portal_assignment(id) ON DELETE SET NULL,
                        notification_type varchar(50) NOT NULL,
                        title varchar(255) NOT NULL,
                        message text NOT NULL,
                        target_audience varchar(30) DEFAULT 'all',
                        is_sent boolean DEFAULT false,
                        sent_at timestamptz,
                        created_at timestamptz NOT NULL DEFAULT now()
                    );
                    CREATE INDEX IF NOT EXISTS idx_assign_notif_assignment ON public.portal_assignment_notification(assignment_id);
                """)
        except Exception:
            pass

    def get(self, request):
        self._ensure_table()
        if not table_exists("portal_assignment_notification"):
            return Response([])
        assignment_id = request.query_params.get("assignment_id")
        sql = """
            SELECT an.id, an.assignment_id, an.notification_type, an.title, an.message,
                   an.target_audience, an.is_sent, an.sent_at, an.created_at,
                   a.title AS assignment_title
            FROM portal_assignment_notification an
            LEFT JOIN portal_assignment a ON a.id = an.assignment_id
        """
        params = []
        if assignment_id:
            sql += " WHERE an.assignment_id = %s"
            params.append(assignment_id)
        sql += " ORDER BY an.created_at DESC"
        return Response(serialise(rows(sql, params)))

    def post(self, request):
        self._ensure_table()
        if not table_exists("portal_assignment_notification"):
            return Response({"detail": "Notification table not found."}, status=400)
        d = request.data
        notification_type = (d.get("notification_type") or "").strip()
        title = (d.get("title") or "").strip()
        message = (d.get("message") or "").strip()
        if not notification_type or not title or not message:
            return Response({"detail": "notification_type, title, and message are required."}, status=400)
        with connection.cursor() as cursor:
            cursor.execute(
                "INSERT INTO portal_assignment_notification "
                "(assignment_id, notification_type, title, message, target_audience) "
                "VALUES (%s,%s,%s,%s,%s) RETURNING id",
                [d.get("assignment_id"), notification_type, title, message,
                 d.get("target_audience", "all")],
            )
            new_id = cursor.fetchone()[0]
        log_action(request.user, "assignment_notification.create", "portal_assignment_notification", new_id, d)
        return Response({"id": new_id, "detail": "Notification created."}, status=201)

    def delete(self, request):
        self._ensure_table()
        if not table_exists("portal_assignment_notification"):
            return Response({"detail": "Table not found."}, status=400)
        rec_id = request.query_params.get("id")
        if not rec_id:
            return Response({"detail": "id is required."}, status=400)
        with connection.cursor() as cursor:
            cursor.execute("DELETE FROM portal_assignment_notification WHERE id=%s RETURNING id", [rec_id])
            deleted = cursor.fetchone()
        if not deleted:
            return Response({"detail": "Not found."}, status=404)
        log_action(request.user, "assignment_notification.delete", "portal_assignment_notification", rec_id, {})
        return Response({"detail": "Deleted."})


