# ---------------------------------------------------------------------------
class AssignmentAnalyticsView(AdminMixin, APIView):
    """GET dashboard analytics: total assignments, pending, submitted,
    average_marks, completion_percentage."""

    def get(self, request):
        analytics = {}
        try:
            with connection.cursor() as cursor:
                if table_exists("portal_assignment"):
                    cursor.execute("SELECT COUNT(*)::int FROM portal_assignment")
                    analytics["total_assignments"] = cursor.fetchone()[0]
                else:
                    analytics["total_assignments"] = 0

                if table_exists("portal_assignment_submission"):
                    cursor.execute("SELECT COUNT(*)::int FROM portal_assignment_submission")
                    analytics["total_submissions"] = cursor.fetchone()[0]
                    cursor.execute("SELECT COALESCE(AVG(marks_obtained), 0)::numeric(7,2) FROM portal_assignment_submission WHERE marks_obtained IS NOT NULL")
                    analytics["average_marks"] = float(cursor.fetchone()[0])
                else:
                    analytics["total_submissions"] = 0
                    analytics["average_marks"] = 0

                if table_exists("portal_assignment"):
                    cursor.execute(
                        """
                        SELECT COUNT(DISTINCT a.id)::int AS total,
                               COUNT(DISTINCT sub.id)::int AS with_submissions
                        FROM portal_assignment a
                        LEFT JOIN portal_assignment_submission sub ON sub.assignment_id = a.id
                        """
                    )
                    counts = cursor.fetchone()
                    total = counts[0] or 0
                    with_sub = counts[1] or 0
                    analytics["pending"] = total - with_sub
                    analytics["submitted"] = with_sub
                    analytics["completion_percentage"] = round((with_sub * 100.0 / total), 1) if total > 0 else 0
                else:
                    analytics["pending"] = 0
                    analytics["submitted"] = 0
                    analytics["completion_percentage"] = 0

        except Exception:
            analytics = {
                "total_assignments": 0,
                "total_submissions": 0,
                "average_marks": 0,
                "pending": 0,
                "submitted": 0,
                "completion_percentage": 0,
            }
        return Response(serialise(analytics))


