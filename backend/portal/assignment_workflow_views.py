class ResubmissionView(AdminMixin, APIView):
    """GET  — list resubmission requests
    POST — approve or reject a resubmission"""

    def _ensure_table(self):
        try:
            with connection.cursor() as cursor:
                cursor.execute("""
                    CREATE TABLE IF NOT EXISTS public.portal_resubmission_request (
                        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        assignment_id integer NOT NULL REFERENCES public.portal_assignment(id) ON DELETE CASCADE,
                        student_id integer NOT NULL REFERENCES public.auth_user(id) ON DELETE CASCADE,
                        reason text NOT NULL DEFAULT '',
                        status varchar(20) NOT NULL DEFAULT 'Pending' CHECK (status IN ('Pending','Approved','Rejected')),
                        reviewed_by integer REFERENCES public.auth_user(id) ON DELETE SET NULL,
                        reviewed_at timestamptz,
                        created_at timestamptz NOT NULL DEFAULT now()
                    );
                    CREATE INDEX IF NOT EXISTS idx_resubmission_assignment ON public.portal_resubmission_request(assignment_id);
                    CREATE INDEX IF NOT EXISTS idx_resubmission_student ON public.portal_resubmission_request(student_id);
                """)
        except Exception:
            pass

    def get(self, request):
        self._ensure_table()
        if not table_exists("portal_resubmission_request"):
            return Response([])
        sql = """
            SELECT rr.id, rr.assignment_id, rr.student_id, rr.reason, rr.status,
                   rr.reviewed_by, rr.reviewed_at, rr.created_at,
                   a.title AS assignment_title,
                   COALESCE(u.first_name || ' ' || u.last_name, u.username) AS student_name,
                   COALESCE(ub.first_name || ' ' || ub.last_name, ub.username) AS reviewed_by_name
            FROM portal_resubmission_request rr
            JOIN portal_assignment a ON a.id = rr.assignment_id
            JOIN auth_user u ON u.id = rr.student_id
            LEFT JOIN auth_user ub ON ub.id = rr.reviewed_by
        """
        status_filter = request.query_params.get("status")
        params = []
        if status_filter:
            sql += " WHERE rr.status = %s"
            params.append(status_filter)
        sql += " ORDER BY rr.created_at DESC"
        return Response(serialise(rows(sql, params)))

    def post(self, request):
        self._ensure_table()
        if not table_exists("portal_resubmission_request"):
            return Response({"detail": "Resubmission table not found."}, status=400)
        d = request.data
        req_id = d.get("id")
        action = d.get("action")  # approve or reject
        if not req_id or action not in ("approve", "reject"):
            return Response({"detail": "id and action (approve/reject) are required."}, status=400)
        existing = row("SELECT id, status FROM portal_resubmission_request WHERE id=%s", [req_id])
        if not existing:
            return Response({"detail": "Request not found."}, status=404)
        if existing["status"] != "Pending":
            return Response({"detail": f"Request already {existing['status'].lower()}."}, status=400)
        new_status = "Approved" if action == "approve" else "Rejected"
        with connection.cursor() as cursor:
            cursor.execute(
                "UPDATE portal_resubmission_request SET status=%s, reviewed_by=%s, reviewed_at=now() WHERE id=%s",
                [new_status, request.user.id, req_id],
            )
        log_action(request.user, f"resubmission.{action}", "portal_resubmission_request", req_id, {})
        return Response({"detail": f"Resubmission {action}d."})


