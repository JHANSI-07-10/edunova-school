        self._ensure_table()
        if not table_exists(self.CATEGORY_TABLE):
            return Response({"detail": "Category table not found."}, status=400)
        d = request.data
        name = (d.get("name") or "").strip()
        if not name:
            return Response({"detail": "name is required."}, status=400)
        description = d.get("description", "")
